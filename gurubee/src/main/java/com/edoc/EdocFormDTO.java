package com.edoc;

public class EdocFormDTO {
	
	private String app_doc; // 문서구분
	private String doc_form; // 문서폼
	private int form_num; // 문서폼번호
	
	public String getApp_doc() {
		return app_doc;
	}
	public void setApp_doc(String app_doc) {
		this.app_doc = app_doc;
	}
	public String getDoc_form() {
		return doc_form;
	}
	public void setDoc_form(String doc_form) {
		this.doc_form = doc_form;
	}
	public int getForm_num() {
		return form_num;
	}
	public void setForm_num(int form_num) {
		this.form_num = form_num;
	}

	
}
